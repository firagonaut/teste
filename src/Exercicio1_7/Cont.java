package Exercicio1_7;


public class Cont {
      private int i;

      
    public Cont(int i){
    	this.i=i;
    }
      
	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}
      
  public int incI() {
	  return i++;
  }
  
  public int zeroI() {
	  return i=0;
  }
			
			
			
	
}
	
